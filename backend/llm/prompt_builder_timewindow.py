def build_prompt_timewindow(
    start_time: str,
    end_time: str,
    seg_text: str,
    stats_text: str,
    state_text: str,
    eff_text: str,
    state_stats_text: str,
    gas_text: str,
    geo_text: str,
    llm_summary: dict
) -> str:
    forward_risk_text = llm_summary.get("前方区段风险提示文本", "无")

    return f"""
请直接编写一份正式的《TBM施工时段工况分析报告》。

写作身份为：TBM施工分析工程师。报告面向工程管理与技术人员，语言应保持客观、克制、完整，符合工程技术报告风格。

【分析时段】
起始时间：{start_time}
结束时间：{end_time}

【总体要求】
1. 直接输出报告正文，不要输出“根据分析”“以下为报告”等引导语。
2. 报告应体现“时段分析”特点，重点关注该时间窗内的运行状态、工况波动、局部异常、效率变化及地质响应，不要按全天总结的口吻泛泛而谈。
3. 所有判断必须基于输入信息，不得虚构。
4. 风险表述必须审慎：
   - “关注”“提示”“可能”“表现出”不等于“已发生”“已确认”；
   - 多源地质融合结果仅反映风险关注程度，不代表地质真值。
5. 若该时段样本量偏少、持续时间较短或统计结果不稳定，应主动说明“该时段结果解释需谨慎”。
6. 不要机械堆砌数字，应以时段特征、变化过程、扰动表现、局部问题和建议为主。
7. 全文保持自然连贯，避免明显 AI 腔、重复表达和字段名堆砌。

【报告标题】
TBM施工时段工况分析报告

【报告结构】
请严格按照以下章节输出，并使用正式报告风格撰写：

一、时段概述
概述该时段在全天施工中的性质，例如属于稳定掘进时段、停机调整时段、状态波动时段或局部异常关注时段，并给出总体判断。

二、运行状态与工况变化
结合停机、过渡、稳定掘进、异常段的分布情况，分析该时段运行节奏及工况转换特征，说明是否存在频繁启停、短时切换或明显扰动。

三、施工状态与效率特征
结合隐含施工状态识别和效率统计，分析该时段主要施工状态类型、负载特征及推进效率表现，重点说明是否存在高负载低速、低负载快速推进或状态不协调现象。

四、稳定性与异常波动分析
围绕状态切换、连续作业能力、参数波动及异常表现，判断该时段施工是否稳定，并说明其对效率和设备运行的可能影响。

五、地质关注与施工响应分析
分析该时段对应区段是否存在较高关注区、多源共同关注区及主要风险类型，并重点讨论地质关注是否与施工响应（如推进速度变化、推力或扭矩变化）表现出一致性。不得将风险提示写成已确认灾害。

六、气体监测情况
客观分析该时段内气体指标表现。若未见明显异常，应明确说明；若存在异常但工程意义不清，应说明解释需谨慎，并需结合现场仪器和工况进一步核实。

七、前方区段提示
基于当前时段结束时的掘进位置，结合前方区段信息，对后续施工关注重点进行说明。该部分属于前方风险提示，不是强制施工指令。语言应体现“建议关注”“建议加强监测”“建议持续跟踪”等辅助决策性质。

八、结论与建议
围绕该时段的运行状态、效率、稳定性、地质关注和气体监测情况进行总结，并提出 2~4 条针对该时段及其后续施工的工程化建议。建议应具体、审慎、可执行，但不能写成绝对化指令。

【风格参考】
可采用如下工程报告表达：
- “该时段整体以稳定掘进为主，但局部存在一定波动。”
- “该时间窗内施工状态切换相对频繁，连续性一般。”
- “局部区段表现出较高关注特征，并与施工参数变化存在一定对应关系。”
- “相关结论主要反映该时段特征，需结合全天过程及现场情况进一步判断。”
- “建议后续施工过程中加强监测与参数跟踪。”

【特别注意】
- 不要逐条复述输入材料；
- 不要照搬原始字段名堆砌；
- 不要把施工状态聚类结果直接等同为真实围岩类别或真实工况；
- 不要把风险提示写成灾害已发生；
- 不要遗漏“前方区段提示”这一节；
- 请输出完整报告正文，使用正式小标题和连贯段落，不要输出列表式答案，不要输出附加说明。

以下为可供写作使用的输入信息，请以结构化摘要为主、文本分析结果为辅进行整合。

【结构化摘要】
{llm_summary}

【基础工况分段】
{seg_text}

【基础统计】
{stats_text}

【施工状态】
{state_text}

【效率统计】
{eff_text}

【状态统计】
{state_stats_text}

【地质分析】
{geo_text}

【气体分析】
{gas_text}

【前方区段风险提示】
{forward_risk_text}
"""